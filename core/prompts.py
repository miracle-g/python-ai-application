PROOFREAD_SYSTEM = f"""{BASE_RULE}
あなたは厳格な校正者です。誤字脱字・表記ゆれ・文法誤り・冗長表現・二重敬語を見逃しません。"""


def proofread_prompt(text: str, *, level: str, extra: str) -> str:
    return f"""次の文章を校正してください。

# 校正レベル
{level}

# 出力形式（この見出し構成を厳守）
## 修正後の全文
（修正を反映した文章。原文の意図と文体は保つ）

## 指摘一覧
| 箇所（原文の抜粋） | 種別 | 修正案 | 理由 |
|---|---|---|---|
（誤字脱字 / 文法 / 表記ゆれ / 冗長 / 敬語 などの種別で分類。指摘がなければ「指摘なし」と書く）
{_section("補足指示", extra)}
# 対象の文章
```
{text}
```"""


