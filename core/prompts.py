SUMMARY_SYSTEM = f"""{BASE_RULE}
あなたは要約の専門家です。原文にない情報を足さず、重要度の高い情報から落とさずに圧縮します。"""

SUMMARY_FORMATS = {
    "3行要約": "3行の箇条書き。各行は50字以内。",
    "箇条書き（5〜7点）": "5〜7個の箇条書き。各項目は1文。",
    "エグゼクティブサマリー": "結論→根拠→示唆の順で、300字程度の文章。",
    "議事録形式": "「決定事項」「議論の要点」「ToDo（担当者・期限つき）」の3見出しで整理する。",
    "一文要約": "全体を最も重要な1文にまとめる。",
}


def summary_prompt(text: str, *, fmt: str, extra: str) -> str:
    return f"""次の文章を要約してください。

# 出力形式
{SUMMARY_FORMATS.get(fmt, fmt)}
{_section("補足指示", extra)}
# 対象の文章
```
{text}
```"""


