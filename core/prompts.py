"""各ツールのシステム指示とプロンプト組み立て。

プロンプトの調整はこのファイルに集約する。
"""

from __future__ import annotations

BASE_RULE = """あなたは日本語のプロのライターです。
- 出力は本文のみ。前置き・後書き・「承知しました」等の相槌は書かない。
- 事実が不明な箇所は捏造せず、[要確認] と明記する。
- 過度な誇張表現や中身のない定型句は避け、具体的に書く。"""


def _section(label: str, value: str) -> str:
    """値が空なら丸ごと省略するセクション。"""
    value = (value or "").strip()
    return f"\n# {label}\n{value}\n" if value else ""


# --------------------------------------------------------------------------
# ブログ記事
# --------------------------------------------------------------------------
BLOG_SYSTEM = f"""{BASE_RULE}
あなたはSEOにも配慮するブログ編集者です。読者の検索意図に答えることを最優先し、
見出し構造の整ったMarkdown記事を書きます。"""


def blog_prompt(
    topic: str,
    *,
    audience: str,
    tone: str,
    length: int,
    keywords: str,
    outline: str,
    extra: str,
) -> str:
    return f"""次の条件でブログ記事をMarkdownで執筆してください。

# テーマ
{topic}

# 条件
- 想定読者: {audience or "一般読者"}
- 文体・トーン: {tone}
- 目安の文字数: 約{length}字（±20%）
- 構成: H1タイトル1つ、H2見出し3〜5つ、必要に応じてH3。冒頭にリード文、最後にまとめ。
- 箇条書きや表を適切に使い、流し読みでも要点が伝わるようにする。
{_section("盛り込むキーワード（自然な形で）", keywords)}{_section("希望する構成・見出し案", outline)}{_section("補足情報・指示", extra)}"""


# --------------------------------------------------------------------------
