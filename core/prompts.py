MAIL_SYSTEM = f"""{BASE_RULE}
あなたは日本のビジネスメールに精通した秘書です。相手との関係性に合った敬語を使い、
簡潔で用件が明確なメールを書きます。"""


def mail_prompt(
    received: str,
    *,
    intent: str,
    relationship: str,
    tone: str,
    my_name: str,
    extra: str,
) -> str:
    return f"""次の受信メールに対する返信文を作成してください。

# 受信メール
```
{received}
```

# 返信の方針
{intent}

# 条件
- 相手との関係: {relationship}
- トーン: {tone}
- 差出人（自分）の署名欄の名前: {my_name or "[要確認]"}
- 件名も「件名: 」から始まる行で提案する（Re: 付きが自然ならそれに従う）。
- 宛名、挨拶、本文、結びの順で構成する。
{_section("補足情報・指示", extra)}"""


